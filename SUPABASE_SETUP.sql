    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    main_title TEXT,
    description TEXT,
    experience TEXT,
    gallery JSONB DEFAULT '[]'::jsonb,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

