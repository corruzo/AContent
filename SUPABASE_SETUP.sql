-- 4. Create table for Services
CREATE TABLE IF NOT EXISTS public.services (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    icon TEXT,
    title TEXT NOT NULL,
    subtitle TEXT,
    description TEXT,
    tag TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 5. Create table for Portfolio
CREATE TABLE IF NOT EXISTS public.portfolio (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    category TEXT,
    location TEXT,
    image_url TEXT,
    gallery JSONB DEFAULT '[]'::jsonb,
    is_wide BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 6. Create table for Partners/Clients
CREATE TABLE IF NOT EXISTS public.partners (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT,
    logo_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 7. Create table for Testimonials
CREATE TABLE IF NOT EXISTS public.testimonials (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    role TEXT,
    content TEXT NOT NULL,
    image_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 8. Create table for Contact Messages
CREATE TABLE IF NOT EXISTS public.messages (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT,
    email TEXT,
    message TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 9. Create table for Analytics (Simplified)
CREATE TABLE IF NOT EXISTS public.analytics (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    session_id TEXT,
    page_path TEXT,
    visitor_info JSONB,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 10. Create table for Admin Access
CREATE TABLE IF NOT EXISTS public.admin_access (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username TEXT DEFAULT 'admin',
    password TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
