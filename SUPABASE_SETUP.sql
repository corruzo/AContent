    full_name TEXT DEFAULT 'Anais Cruz',
    seo_description TEXT,
    logo_url TEXT,
    email TEXT,
    phone TEXT,
    address TEXT,
    instagram_url TEXT,
    linkedin_url TEXT,
    footer_tagline TEXT,
    services_bg_url TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

